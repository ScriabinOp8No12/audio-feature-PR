import { GobanConfig } from "goban";
import { LearningPage, LearningPageProperties } from "../../LearningPage";
import { _, pgettext } from "@/lib/translate";
import { LearningHubSection } from "../../LearningHubSection";

export class HangingConnection extends LearningHubSection {
    static pages(): Array<typeof LearningPage> {
        return [Page01, Page02, Page03, Page04, Page05, Page06];
    }
    static section(): string {
        return "connect_with_hanging_connection";
    }
    static title(): string {
        return pgettext(
            "Tutorial section name on learning connect with hanging connection",
            "Hanging connection",
        );
    }
    static subtext(): string {
        return pgettext(
            "Tutorial section subtext on learning on connect with hanging connection",
            "Connect with hanging connection",
        );
    }
}

class Page01 extends LearningPage {
    constructor(props: LearningPageProperties) {
        super(props);
    }

    text() {
        return _("White to play. Connect the white stones with a hanging connection.");
    }
    config(): GobanConfig {
        return {
            width: 9,
            height: 9,
            mode: "puzzle",
            initial_player: "white",
            initial_state: {
                black: "ffcgfggg",
                white: "gdgfhg",
            },
            move_tree: this.makePuzzleMoveTree(["he"], [], 9, 9),
        };
    }
}

