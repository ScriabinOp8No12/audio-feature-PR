import { GobanConfig } from "goban";
import { LearningPage, LearningPageProperties } from "../../LearningPage";
import { _, pgettext } from "@/lib/translate";
import { LearningHubSection } from "../../LearningHubSection";

export class Connect extends LearningHubSection {
    static pages(): Array<typeof LearningPage> {
        return [Page01, Page02, Page03, Page04, Page05, Page06];
    }
    static section(): string {
        return "connect";
    }
    static title(): string {
        return pgettext("Tutorial section name on learning connect", "Connect");
    }
    static subtext(): string {
        return pgettext("Tutorial section subtext on learning on connect", "Connect your stones");
    }
}
class Page01 extends LearningPage {
    constructor(props: LearningPageProperties) {
        super(props);
    }

    text() {
        return _(
            "Stones can help each other by forming a chain. Chains are harder to capture then single stones. You can form a chain by connecting your stones. Connect the white stones.",
        );
    }
    config(): GobanConfig {
        return {
            width: 9,
            height: 9,
            mode: "puzzle",
            initial_player: "white",
            initial_state: {
                black: "dcddcfdf",
                white: "beceee",
            },
            marks: { cross: "de" },
            move_tree: this.makePuzzleMoveTree(["de"], [], 9, 9),
        };
    }
}

